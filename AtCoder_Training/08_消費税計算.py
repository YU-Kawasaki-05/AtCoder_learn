"""
AtCoder ABC問題集 - 標準入出力・演算処理

問題:
X円のものを消費税T%で買うとき、支払い金額は何円になるでしょうか？
ただし、1円未満の端数は切り捨てとします。

入力:
X T

出力:
支払い金額（消費税込み、1円未満切り捨て）

制約:
1 <= X <= 10000
1 <= T <= 100

入力例1:
100 10

出力例1:
110

入力例2:
99 10

出力例2:
108
(99 * 1.1 = 108.9 ⇒ 108)
"""

def solve():
    # 入力を受け取る
    X, T = map(int, input().split())
    
    # 消費税込みの金額を計算（切り捨て）
    price_with_tax = int(X * (1 + T / 100))
    
    # 結果を出力
    print(price_with_tax)

if __name__ == "__main__":
    solve()
